  void charger_Categories_ComboBox(){
   
     ResultSet result=db.db_Con.extraireData("select * from categorie");
     
     ui_ch_categ.addItem(new Categorie());
     Categorie et=null;
     
     if(result!=null)
         try {
             while(result.next())
              {
                et=new Categorie(result.getString("nom"),result.getFloat("prix"));
                et.setCatID(result.getInt("catID"));
                
                 ui_ch_categ.addItem(et);
             
             }
                 } catch (SQLException ex) {
                     
         Logger.getLogger(UI_Chambres.class.getName()).log(Level.SEVERE, null, ex);
     }}  
  
  
   void charger_Etages_ComboBox(){
   
     ResultSet result=db.db_Con.extraireData("select * from etages");
     ui_ch_et.addItem(new Etages());
     Etages et=null;
     
     if(result!=null)
         try {
             while(result.next())
              {
                et=new Etages();
                
                et.setNumeroEtage(result.getInt("numero_etage"));
                et.setEtID(result.getInt("etID"));
                 ui_ch_et.addItem(et);
             
             }
                 } catch (SQLException ex) {
         Logger.getLogger(UI_Chambres.class.getName()).log(Level.SEVERE, null, ex);
     }}  
  
