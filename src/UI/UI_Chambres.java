    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Categorie cat=(Categorie)ui_ch_categ.getSelectedItem();
        Etages et=(Etages)ui_ch_et.getSelectedItem();
        
        if(cat.getCatID()==0)
        {  
             Generalites.Donner_info_User("Choisissez s'il vous plait la categorie");
            return;
        }
        
        if(et.getEtID()==0)
        {
         Generalites.Donner_info_User("Choisissez s'il vous plait le numero de l'etage");
            return;
        }
        
        
        Chambre ch=new Chambre();
        
           ch.setCatID(cat);
           ch.setEtID(et);
           ch.setNumero_ch(UI_Chambres.num_ch);
           
           if(ch.creerChambre()>0){
           
            Generalites.Donner_info_User("Felicitation, la chambre vient d'etre cree");
           }
           else Generalites.Donner_info_User("Desole, la chambre n'a pas ete cree");
           
           
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

